import 'result.dart';

class Videos {
  List<Result>? results;

  Videos({this.results});

  factory Videos.fromJson(Map<String, dynamic> json) => Videos(
        results: (json['results'] as List<dynamic>?)
            ?.map((e) => Result.fromJson(e as Map<String, dynamic>))
            .toList(),
      );

  Map<String, dynamic> toJson() => {
        'results': results?.map((e) => e.toJson()).toList(),
      };
}
